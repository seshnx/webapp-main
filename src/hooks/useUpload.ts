/**
 * Image upload hook (25MB max, image types only).
 */
export interface UseImageUploadReturn {
  uploadImage: (
    file: File | null,
    folder?: StorageFolder
  ) => Promise<UploadResult | null>;
  uploading: boolean;
  error: string | null;
  isUploading: boolean;
  hasError: boolean;
}

export function useImageUpload(options: UploadOptions = {}): UseImageUploadReturn {
  const { uploadMedia, uploading, error } = useUpload();

  const uploadImage = async (
    file: File | null,
    folder: StorageFolder = STORAGE_FOLDERS.PROFILE_PHOTOS
  ): Promise<UploadResult | null> => {
    if (!file) return null;

    if (!isImageFile(file)) {
      console.error("File is not an image");
      return null;
    }

    const imageOptions: UploadOptions = {
      maxSizeMB: UPLOAD_LIMITS.IMAGE_MAX_MB,
      allowedTypes: [...UPLOAD_LIMITS.ALLOWED_IMAGE_TYPES],
      ...options,
    };

    return await uploadMedia(file, folder, imageOptions);
  };

  return {
    uploadImage,
    uploading,
    error,
    isUploading: uploading,
    hasError: !!error,
  };
}

