/**
 * Upload with local preview generation for images.
 */
export interface UseUploadWithPreviewReturn {
  uploadWithPreview: (
    file: File | null,
    folder?: StorageFolder
  ) => Promise<UploadResult | null>;
  preview: string | null;
  uploading: boolean;
  error: string | null;
  clearPreview: () => void;
  isUploading: boolean;
  hasError: boolean;
}

export function useUploadWithPreview(): UseUploadWithPreviewReturn {
  const [preview, setPreview] = useState<string | null>(null);
  const { uploadMedia, uploading, error } = useUpload();

  const uploadWithPreview = async (
    file: File | null,
    folder: StorageFolder = STORAGE_FOLDERS.POST_MEDIA
  ): Promise<UploadResult | null> => {
    if (!file) return null;

    // Create local preview for images
    if (isImageFile(file)) {
      const reader = new FileReader();
      reader.onloadend = () => {
        setPreview(reader.result as string);
      };
      reader.readAsDataURL(file);
    }

    // Upload the file
    const result = await uploadMedia(file, folder);

    // Update preview with uploaded URL
    if (result) {
      setPreview(result.url);
    }

    return result;
  };

  const clearPreview = (): void => {
    setPreview(null);
  };

  return {
    uploadWithPreview,
    preview,
    uploading,
    error,
    clearPreview,
    isUploading: uploading,
    hasError: !!error,
  };
}

export default useUpload;
